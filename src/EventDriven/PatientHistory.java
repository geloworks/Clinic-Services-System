    private void loadPatientRecords(int cleanId) {
        try {
            ps = conn.prepareStatement("SELECT RecordID, Service, Doctor, Diagnosis, Prescription, Date "
                    + "FROM PatientRecords WHERE PatientID = ? ORDER BY Date DESC");
            ps.setInt(1, cleanId);
            rs = ps.executeQuery();

            DefaultTableModel dm = (DefaultTableModel) historyTable.getModel();
            dm.setRowCount(0);

            while (rs.next()) {
                Vector<Object> v1 = new Vector<>();
                v1.add(rs.getInt("RecordID"));
                v1.add(rs.getString("Service"));
                v1.add(rs.getString("Doctor"));
                v1.add(rs.getString("Diagnosis"));
                v1.add(rs.getString("Prescription"));
                v1.add(rs.getDate("Date"));
                dm.addRow(v1);
            }

        } catch (SQLException e) {
            System.out.println("" + e);
        }
    }

