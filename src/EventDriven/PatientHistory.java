    private void printPatientHistoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printPatientHistoryActionPerformed
        // TODO add your handling code here:

        try {
            int cleanId = Integer.parseInt(this.currentPatientId.replace("P-", ""));
            HashMap<String, Object> parameters = new java.util.HashMap<>();
            ps = conn.prepareStatement("SELECT FirstName, LastName, Sex, Age, Weight, Height, ContactNumber "
                    + "FROM Patient WHERE PatientID = ?");
            ps.setInt(1, cleanId);
            rs = ps.executeQuery();

            if (rs.next()) {
                parameters.put("pName", rs.getString("FirstName") + " " + rs.getString("LastName"));
                parameters.put("pSex", rs.getString("Sex"));
                parameters.put("pAge", rs.getInt("Age"));
                parameters.put("pWeight", rs.getDouble("Weight"));
                parameters.put("pHeight", rs.getDouble("Height"));
                parameters.put("pContact", rs.getString("ContactNumber"));
                parameters.put("patientId", cleanId);
            }
            JasperReport patientReport = (JasperReport) JRLoader.loadObjectFromFile("src\\reports\\PatientHistory.jasper");
            JasperPrint reportPrint = JasperFillManager.fillReport(patientReport, parameters, conn);
            JasperViewer viewer = new JasperViewer(reportPrint, false);
            javax.swing.JDialog reportDialog = new javax.swing.JDialog(this, "Print Preview", true);
            reportDialog.setContentPane(viewer.getContentPane());
            reportDialog.setSize(1000, 900);
            reportDialog.setLocationRelativeTo(this);
            reportDialog.setVisible(true);

        } catch (Exception ex) {
            System.out.println(ex);
        }
    }//GEN-LAST:event_printPatientHistoryActionPerformed

